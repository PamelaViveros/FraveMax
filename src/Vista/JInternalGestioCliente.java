    private void limpiarTxt() {
        jtxt_apellido.setText("");
        jtxt_nombre.setText("");
        jtxt_dni.setText("");
        jtxt_direccion.setText("");
        jtxt_telefono.setText("");
    }

    private void encabezadoTabla() {
        ArrayList<Object> encabezado = new ArrayList<>();
        encabezado.add("ID");
        encabezado.add("Nombre");
        encabezado.add("Apellido");
        encabezado.add("Dni");
        encabezado.add("DomicÃ­lio");
        encabezado.add("Telefono");

        for (Object col : encabezado) {
            modelo.addColumn(col);
        }
        this.jTable_cliente.setModel(modelo);
    }
    
    private void borrarFilas() {
        int filas = jTable_cliente.getRowCount() - 1;
        for (int i = filas; i >= 0; i--) {
            modelo.removeRow(i);
        }
    }

    private void llenarTablaCliente() {
        List<Cliente> clientes = cData.listaClientes();

        for (Cliente cliente: clientes) {
            modelo.addRow(new Object[]{
                cliente.getIdCliente(),
                cliente.getNombre(),
                cliente.getApellido(),
                cliente.getDni(),
                cliente.getDomicilio(),
                cliente.getTelefono(),
            });
        }

        jTable_cliente.addMouseListener(new MouseAdapter() {

            @Override
            public void mouseClicked(MouseEvent e) {
                int fila_p = jTable_cliente.rowAtPoint(e.getPoint());
                int columna_p = 0;

                if (fila_p > -1) {
                    idCliente = (int) modelo.getValueAt(fila_p, columna_p);
                    datosSeleccionados(idCliente);
                }
            }
        });
    }

    private void datosSeleccionados(int idCliente) {
        Cliente cliente = cData.buscarClienteXid(idCliente);

        jtxt_apellido.setText(cliente.getApellido());
        jtxt_nombre.setText(cliente.getNombre());                
        jtxt_dni.setText(String.valueOf(cliente.getDni()));
        jtxt_direccion.setText(cliente.getDomicilio());
        jtxt_telefono.setText(String.valueOf(cliente.getTelefono()));
    }
