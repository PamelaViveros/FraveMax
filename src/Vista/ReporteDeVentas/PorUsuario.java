public void cargaCombo(){
    
    for (Usuario usuario : usuarios) {
        jcbUsuarios.addItem(usuario);
    }
    
    usuarios.clear();
}

public void cabecera(){
    
    modelo.addColumn("id Venta");
    modelo.addColumn("NÂº Detalle");
        modelo.addColumn("idCliente");
        modelo.addColumn("Cantidad");
        modelo.addColumn("Nombre producto");
        modelo.addColumn("Fecha de venta");
    tListaVentas.setModel(modelo);
    
}

public void cargaTablaxUsuario(){
    Usuario user= (Usuario)jcbUsuarios.getSelectedItem();
    
    vData.ventasPorUsuario(user.getIdUsuario());

        for (Venta venta : ventas) {
           dvData.detalles(venta.getIdVenta());
             LocalDate fecha=venta.getFechaVenta();
            String fechaOk=fecha.format(dateTimeFormatter);
            
            for (DetalleVenta detalle : detalles) {
            Producto prod=pData.buscarPorId(detalle.getIdProducto());
                modelo.addRow(new Object[]{
                venta.getIdVenta(),
                    detalle.getIdDetalleVenta(),
                venta.getIdCliente(),
                detalle.getIdDetalleVenta(),    
                detalle.getCantidad(),
               prod.getNombreProducto(),
                fechaOk,
                
                });
            }
           
            detalles.clear();
        }
        ventas.clear();
    
    
}

 public void borrarFilas() {
        int f = (tListaVentas.getRowCount() - 1);
        for (; f >= 0; f--) {
            modelo.removeRow(f);
        }
    }
