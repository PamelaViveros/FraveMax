    public void cabecera() {
        modelo.addColumn("id Venta");
        modelo.addColumn("idCliente");
        modelo.addColumn("Producto");
        modelo.addColumn("Cantidad");
        modelo.addColumn("id Usuario");
        modelo.addColumn("Fecha venta");
        tListaVentas.setModel(modelo);
    }

    public void cargaTabla() {
        int a単o = 0, mes = -1, dia = 0;
        a単o = jcFecha.getCalendar().get(Calendar.YEAR);
        mes = jcFecha.getCalendar().get(Calendar.MARCH);
        dia = jcFecha.getCalendar().get(Calendar.DAY_OF_MONTH);
        LocalDate fecha = LocalDate.of(a単o, (mes + 1), dia);

String fechaOk = fecha.format(dateTimeFormatter);  //17-02-2022
        if (a単o != 0 && mes != -1 && dia != 0) {
            vData.buscarVentasxFecha(fecha);

            for (Venta venta : ventas) {
                DetalleVenta dv = dvData.detallarVenta(venta.getIdVenta());
                Producto prod = pData.buscarPorId(dv.getIdProducto());
                modelo.addRow(new Object[]{
                    venta.getIdVenta(),
                    venta.getIdCliente(),
                    prod.getNombreProducto(),
                    dv.getCantidad(),
                    venta.getIdUsuario(),
                    fechaOk
                });
            }
        }
        ventas.clear();

    }

    public void cargaTodo() {
        vData.listarVentas();

String fechaOk;
        for (Venta venta : ventas) {
            DetalleVenta dv = dvData.detallarVenta(venta.getIdVenta());
            Producto prod = pData.buscarPorId(dv.getIdProducto());
            LocalDate fecha=venta.getFechaVenta();
            fechaOk=fecha.format(dateTimeFormatter);
            modelo.addRow(new Object[]{
                venta.getIdVenta(),
                venta.getIdCliente(),
                prod.getNombreProducto(),
                dv.getCantidad(),
                venta.getIdUsuario(),
                 fechaOk
               
            });
        }
        ventas.clear();
    }

    public void borrarFilas() {
        int f = (tListaVentas.getRowCount() - 1);
        for (; f >= 0; f--) {
            modelo.removeRow(f);
        }
    }

